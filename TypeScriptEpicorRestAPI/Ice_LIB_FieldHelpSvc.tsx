   /**  
   Summary: Invoke method GetFieldHelp
   Description: Returns the field help text from the database.
   OperationID: GetFieldHelp
      @param epicorHeaders A string representing the epicor log in information to be used, 
         already converted to base64 in the format username:password, defaults to the configEpicorSchemas creds
      @param requestBody Desc: Input parameters  => reference#/components/schemas/GetFieldHelp_input
   Returns: 
      200 Desc: OK => reference#/components/schemas/GetFieldHelp_output
      500 Desc: Internal server error. Server is unable to process the request.
   */  
export function post_GetFieldHelp(requestBody:GetFieldHelp_input, epicorHeaders?:Headers){

   var headers = configEpicorSchemas.epicorHeaders
   if(typeof epicorHeaders !== 'undefined'){
         headers = epicorHeaders
   }

   return (new Promise<GetFieldHelp_output>((resolve, reject) => {
      const request: RequestInfo = new Request(configEpicorSchemas.epicorURL + "Ice.LIB.FieldHelpSvc/GetFieldHelp", {
          method: 'post',
          headers: headers,
          body: JSON.stringify(requestBody)
      })
      fetch(request)
      .then((res) => {
         if(res.ok){
             return res.json()
         }
         else{
             return res.json().then(text => {throw new Error(text["ErrorMessage"]) })
         }
      })
      .then((data) => {
         resolve(data as GetFieldHelp_output)
          })
      .catch((error) => {
          reject(error)
      })
   }))
}



